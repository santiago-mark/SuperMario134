int main(void)
{
	/* Initialize SDL */
	if (SDL_Init(SDL_INIT_VIDEO) < 0) {
		return 1;
	}
	/* Create the window, OpenGL context */
	SDL_GL_SetAttribute(SDL_GL_BUFFER_SIZE, 32);
	SDL_GL_SetAttribute(SDL_GL_DOUBLEBUFFER, 1);
	AABB camera; AABB tile;
	Uint32 lastFrameMs;
	Uint32 currentFrameMs = SDL_GetTicks();
	SDL_Window* window = SDL_CreateWindow(
		"TestSDL",
		SDL_WINDOWPOS_UNDEFINED, SDL_WINDOWPOS_UNDEFINED,
		640, 480,
		SDL_WINDOW_OPENGL);
	if (!window) {
		fprintf(stderr, "Could not create window.ErrorCode = %s\n", SDL_GetError());
		SDL_Quit();
		return 1;
	}
	SDL_GL_CreateContext(window);

	/* Make sure we have a recent version of OpenGL */
	GLenum glewError = glewInit();
	if (glewError != GLEW_OK) {
		fprintf(stderr, "Could not initialize glew.ErrorCode = %s\n", glewGetErrorString(glewError));
		SDL_Quit();
		return 1;
	}
	if (!GLEW_VERSION_3_0) {
		fprintf(stderr, "OpenGL max supported version is too low.\n");
		SDL_Quit();
		return 1;
	}
	/* Setup OpenGL state */
	glViewport(0, 0, 640, 480);
	glMatrixMode(GL_PROJECTION);
	glOrtho(0, 640, 480, 0, 0, 100);
	glEnable(GL_TEXTURE_2D);
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

	/* The previous frame's keyboard state */
	unsigned char kbPrevState[SDL_NUM_SCANCODES] = { 0 };
	/* The current frame's keyboard state */
	const unsigned char* kbState = NULL;
	/* Keep a pointer to SDL's internal keyboard state */
	kbState = SDL_GetKeyboardState(NULL);

	GLuint tiles[21] = { 
		glTexImageTGAFile("Mario/00.tga", NULL, NULL),
		glTexImageTGAFile("Mario/01.tga", NULL, NULL),
		glTexImageTGAFile("Mario/02.tga", NULL, NULL),
		glTexImageTGAFile("Mario/03.tga", NULL, NULL),
		glTexImageTGAFile("Mario/04.tga", NULL, NULL),
		glTexImageTGAFile("Mario/05.tga", NULL, NULL),
		glTexImageTGAFile("Mario/06.tga", NULL, NULL),
		glTexImageTGAFile("Mario/07.tga", NULL, NULL),
		glTexImageTGAFile("Mario/08.tga", NULL, NULL),
		glTexImageTGAFile("Mario/09.tga", NULL, NULL),
		glTexImageTGAFile("Mario/10.tga", NULL, NULL),
		glTexImageTGAFile("Mario/11.tga", NULL, NULL),
		glTexImageTGAFile("Mario/12.tga", NULL, NULL),
		glTexImageTGAFile("Mario/13.tga", NULL, NULL),
		glTexImageTGAFile("Mario/block.tga", NULL, NULL),
		glTexImageTGAFile("Mario/block2.tga", NULL, NULL),
		glTexImageTGAFile("Mario/ent01.tga", NULL, NULL),
		glTexImageTGAFile("Mario/ent02.tga", NULL, NULL),
		glTexImageTGAFile("Mario/ent03.tga", NULL, NULL),
		glTexImageTGAFile("Mario/ent04.tga", NULL, NULL),
		glTexImageTGAFile("Mario/cannon.tga", NULL, NULL)
	};

	skyBackground = glTexImageTGAFile("Mario/background/skybackground.tga", NULL, NULL);
	questionBoxHit = glTexImageTGAFile("Mario/box3.tga", NULL, NULL);

	GLuint questionBoxFrame[3] = {
		glTexImageTGAFile("Mario/box0.tga", NULL, NULL),
		glTexImageTGAFile("Mario/box1.tga", NULL, NULL),
		glTexImageTGAFile("Mario/box2.tga", NULL, NULL)
	};
	GLuint piranhaPlant_AnimFrame[2] = {
		glTexImageTGAFile("Mario/piranha_plant1.tga", NULL, NULL),
		glTexImageTGAFile("Mario/piranha_plant2.tga", NULL, NULL)
	};

	GLuint coin_AnimFrame[3] = {
		glTexImageTGAFile("Mario/coin00.tga", NULL, NULL),
		glTexImageTGAFile("Mario/coin01.tga", NULL, NULL),
		glTexImageTGAFile("Mario/coin02.tga", NULL, NULL)
	};
	/********************************************************Animation Definitions***********************************************************/
	/*animation definitions of question box*/
	AnimDef questionBox_AnimDef{
		"box",
		{ { 0, .1 }, { 1, .1 }, { 2, .1 }, { 1, .1 } },
		20,
		4
	};
	/**************************************************************Animation Data************************************************************/
	/*animation of question box*/
	AnimData questionBox_AnimData = {
		&questionBox_AnimDef,
		0,
		0.1,
		true
	};
	/****************************************************Set animation********************************************************************************/
	animSet(&questionBox_AnimData, &questionBox_AnimDef);
	animSet(&piranhaPlant_AnimData, &piranhaPlant_AnimDef);
	animSet(&coin_AnimData, &coin_AnimDef);

	/* The game loop */
	while (!shouldExit) {
		/******************************************************Save the last Frame's value******************************************************/
		lastFrameMs = currentFrameMs;
		// Handle OS message pump
		/* kbState is updated by the message pump. Copy over the old state before the pump! */
		memcpy(kbPrevState, kbState, sizeof(kbPrevState));
		currentFrameMs = SDL_GetTicks();
		float deltaTime = (currentFrameMs - lastFrameMs) / 1000.0f;
		/******************************************************OS message Pump******************************************************************/
		SDL_Event event;
		while (SDL_PollEvent(&event)) {
			switch (event.type) {
			case SDL_QUIT:
				shouldExit = 1;
			}
		}
		if (kbState[SDL_SCANCODE_W]){
			if (camY > 0){
				camY--;
			}
		}
		if (kbState[SDL_SCANCODE_S]){
			if (camY < (WORLD_SIZEY - 480)){
				camY++;
			}
		}
		if (kbState[SDL_SCANCODE_A]){
			if (camX > 0){
				camX--;
			}
		}
		if (kbState[SDL_SCANCODE_D]){
			if (camX < (WORLD_SIZEX - 640)){
				camX++;
			}
		}
		camera = { camX, camY, 640, 480 };

		/******************************************************Update and draw game*************************************************************/
		animTick(&questionBox_AnimData, deltaTime);
		animTick(&piranhaPlant_AnimData, deltaTime);
		animTick(&coin_AnimData, deltaTime);

		glClearColor(0, 0, 0, 1);
		glClear(GL_COLOR_BUFFER_BIT);
		/*draw sky background*/
		glDrawSprite(skyBackground, 0, 0, 640, 480);
		/*draws the background1 of the game*/
		int i, j, tileX, tileY;
		for (i = 0; i < numOfTileX; i++){
			for (j = 0; j < numOfTileY; j++){
				tileX = i * TILE_SIZEX;
				tileY = j * TILE_SIZEY;
				tile = { tileX, tileY, TILE_SIZEX, TILE_SIZEY };
				if (AABBIntersect(&camera, &tile))
					glDrawSprite(tiles[map[i][j]], tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY);
			}
		}
		/*draws exit*/
		for (i = 0; i < 2; i++){
			for (j = 0; j < 2; j++){
				tileX = TILE_SIZEX*(i + 171);
				tileY = TILE_SIZEY*(j + 22);
				tile = { tileX, tileY, TILE_SIZEX, TILE_SIZEY };
				if (AABBIntersect(&camera, &tile))
					glDrawSprite(tiles[exitEnt[i][j]], tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY);
			}
		}
		/*draws all hidden boxes*/
		for (i = 0; i < sizeof(boxes_in_map)/sizeof(boxes_in_map[0]); i++){
			tileX = TILE_SIZEX*boxes_in_map[i].x;
			tileY = TILE_SIZEY*boxes_in_map[i].y;
			tile = { tileX, tileY, TILE_SIZEX, TILE_SIZEY };
			if (AABBIntersect(&camera, &tile)){
				if (!boxes_in_map[i].hidden){
					if (!boxes_in_map[i].hit){
						animDraw(&questionBox_AnimData, tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY, questionBoxFrame);
						if (!questionBox_AnimData.isPlaying)
							animReset(&questionBox_AnimData);
					}
					else{
						glDrawSprite(questionBoxHit, tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY);
					}
				}
			}
		}
		/*draw all piranha plants*/
		for (int i = 0; i < sizeof(piranhaPlant_in_map) / sizeof(piranhaPlant_in_map[0]); i++){
			tileX = TILE_SIZEX*piranhaPlant_in_map[i].x;
			tileY = TILE_SIZEY*piranhaPlant_in_map[i].y;
			tile = { tileX, tileY, TILE_SIZEX, TILE_SIZEY };
			if (AABBIntersect(&camera, &tile)){
					if (!piranhaPlant_in_map[i].hit){
						animDraw(&piranhaPlant_AnimData, tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY, piranhaPlant_AnimFrame);
						if (!piranhaPlant_AnimData.isPlaying)
							animReset(&piranhaPlant_AnimData);
					}
			}
		}
		/*draw all coins*/		
		for (int i = 0; i < sizeof(coins_in_map) / sizeof(coins_in_map[0]); i++){
			tileX = TILE_SIZEX*coins_in_map[i].x;
			tileY = TILE_SIZEY*coins_in_map[i].y;
			tile = { tileX, tileY, TILE_SIZEX, TILE_SIZEY };
			if (AABBIntersect(&camera, &tile)){
				if (!coins_in_map[i].taken){
					animDraw(&coin_AnimData, tileX - camX, tileY - camY, TILE_SIZEX, TILE_SIZEY, coin_AnimFrame);
					if (!coin_AnimData.isPlaying)
						animReset(&coin_AnimData);
				}
			}
		}

		/* Present to the player */
		SDL_GL_SwapWindow(window);
	}
}